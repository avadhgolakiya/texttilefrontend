/**
 * Generates public/firebase-messaging-sw.js from .env.local values.
 * Run automatically before dev/build.
 */
import { readFileSync, writeFileSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

function loadEnv() {
  const envPath = join(root, '.env.local');
  const vars = {};
  if (!existsSync(envPath)) return vars;

  for (const line of readFileSync(envPath, 'utf8').split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const idx = trimmed.indexOf('=');
    if (idx === -1) continue;
    const key = trimmed.slice(0, idx).trim();
    const value = trimmed.slice(idx + 1).trim().replace(/^["']|["']$/g, '');
    vars[key] = value;
  }
  return vars;
}

const env = loadEnv();

const config = {
  apiKey: env.NEXT_PUBLIC_FIREBASE_API_KEY ?? '',
  authDomain: env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN ?? '',
  projectId: env.NEXT_PUBLIC_FIREBASE_PROJECT_ID ?? '',
  storageBucket: env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET ?? '',
  messagingSenderId:
    env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID ?? '132788988984',
  appId: env.NEXT_PUBLIC_FIREBASE_APP_ID ?? '',
};

const sw = `/* Auto-generated by scripts/sync-firebase-sw.mjs — do not edit manually */
importScripts('https://www.gstatic.com/firebasejs/10.14.1/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.14.1/firebase-messaging-compat.js');

firebase.initializeApp(${JSON.stringify(config, null, 2)});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const title = payload.notification?.title ?? 'Swastik Fashion';
  const body = payload.notification?.body ?? 'New update available';
  const link = payload.fcmOptions?.link ?? payload.data?.link ?? '/home';

  self.registration.showNotification(title, {
    body,
    icon: '/icon-192.png',
    data: { link },
  });
});

self.addEventListener('notificationclick', (event) => {
  event.notification.close();
  const link = event.notification.data?.link ?? '/home';
  const absoluteUrl = new URL(link, self.location.origin).href;
  event.waitUntil(clients.openWindow(absoluteUrl));
});
`;

writeFileSync(join(root, 'public', 'firebase-messaging-sw.js'), sw);
console.log('[sync-firebase-sw] Generated public/firebase-messaging-sw.js');
